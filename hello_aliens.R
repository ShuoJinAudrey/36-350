Talking to intelligent extraterrestrials from an newly discorvered
habitable-zone, Earth-like exoplanets would not be a good idea.
What if the extraterrestrials possess more powerful technologies and
weapons than us and they intend to conquer the Earth? What if they are
hypocritical and we have no idea? Limited communications can hinder
our predictions and understandings of intelligent extraterrestrials and
their planet, and it can lead to a catastrophe.
Maybe, we just need to stay silent.
